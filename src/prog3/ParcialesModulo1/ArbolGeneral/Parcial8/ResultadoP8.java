package prog3.ParcialesModulo1.ArbolGeneral.Parcial8;

public class ResultadoP8 {

    private int caminosPares;
    private int caminosImpares;


    public ResultadoP8(){
        caminosPares=0;
        caminosImpares=0;
    }

    public int getCaminosPares() {
        return caminosPares;
    }

    public void setCaminosPares(int caminosPares) {
        this.caminosPares = caminosPares;
    }

    public int getCaminosImpares() {
        return caminosImpares;
    }

    public void setCaminosImpares(int caminosImpares) {
        this.caminosImpares = caminosImpares;
    }
}
